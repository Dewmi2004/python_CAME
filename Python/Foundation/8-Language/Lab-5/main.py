print("Operators & Expressions")
#  Expressions in python is a combination of values, variables, and operators that are evaluated to produce another value.
print(f'power of 2^3 is {2**3=}')  # 2 raised to the power 3

print(f'sign operator +3 is {+3=}')  # positive sign
print(f'sign operator -3 is {-3=}')  # negative sign

print(f'Expression {-5**2=}')  # negative sign with power operator
print(-5**2)  # Output: -25
print((-5)**2)  # Output: 25 because of parentheses

x = int(input("Enter a number: "))
print(f'Value of x is {x**2=}')